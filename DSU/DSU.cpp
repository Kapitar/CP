    vector<int> roots;
    vector<int> sizes;

    explicit DSU(int n = 1) {
        roots.resize(n);
        sizes.resize(n, 1);

        iota(all(roots), 0ll);
    }

    int find_root(int v) {
        if (roots[v] == v) return v;
        return roots[v] = find_root(roots[v]);
    }

    void unite(int a, int b) {
        a = find_root(a);
        b = find_root(b);

        if (a == b) return;
        if (sizes[a] < sizes[b]) swap(a, b);
        sizes[a] += sizes[b];
        roots[b] = a;
    }
